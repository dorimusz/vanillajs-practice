const stringNum = "2"
const string = "aaa"
const num = 2;


//parseInt - parses a value as a string and returns an integer
console.log(parseInt(stringNum))
console.log(parseInt(string))
console.log(parseInt(num))

//parseFloat - parses a value as a string and returns the first integer (egész szám), +, -, 0
console.log(parseFloat(stringNum))
console.log(parseFloat(string))
console.log(parseFloat(num)) 